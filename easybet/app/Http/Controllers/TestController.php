     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Test $test)
    {
        $test->update($request->all());
 
        return response()->json($test, 200);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Test  $test
     * @return \Illuminate\Http\Response
     */
